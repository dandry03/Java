public class IfElse {
        public static void main(String[] args) {
                int grade = 100;

                if (grade >= 80) {
                        System.out.println("Great job, you got an A!");
                } else if (grade >= 70) {
                        System.out.println("Great job, you got a B!");
                } else if (grade >= 60) {
                        System.out.println("You got a C.");
                } else {
                        System.out.println("You got a F. Study hard next time.");
                }
        }
}
